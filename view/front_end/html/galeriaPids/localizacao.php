		<p class = "pTitulo">
						O início do projeto!
					</p>
										
					<p class="pText"><br>
						O projeto PIDS Tech, teve seu start inicial em uma turma do PRONATEC, em 
						2014.  <br>
						Na época, a escola Senac Tech, tinha iniciado a pouco tempo os cursos Técnicos em 
						informática, e nesse curso, o primeiro módulo, são desenvolvidos com os alunos, os 
						conhecimentos técnicos na área da manutenção de computadores. <br><br>
						Na época, um dos conhecimentos abordados com os alunos, era relacionado a 
						questões sustentáveis, e como as empresas não estavam conseguindo descartar 
						corretamente os equipamentos eletrônicos fora de uso. <br><br>
					

						Apresentando esse problema para turma, um dos alunos da época, trabalhava 
						em uma empresa, ao qual, estava com esse problema, pois tinham muitos 
						equipamentos parados em uma sala. <br><br>
						Nesse momento surgiu a ideia de receber da empresa esses equipamentos, 
						através do conhecimento técnico dos alunos, realizar a manutenção e recuperação 
						desses equipamentos, e realizar a doação dos equipamentos recuperados para alguma 
						associação. <br><br>

						Após a recuperação dos computadores, a associação escolhida para receber a doação, foi a 
						AMAVTRON (Associação de moradores da vila tronco neves), que fica localizada no bairro Santa Tereza 
						em Porto Alegre. <br><br>
						O professor Miguel em contato com a associação, verificou o interesse da associação em receber 
						as doações dos computadores, com a finalidade de montar um laboratório de informática para os alunos 
						da associação e as pessoas da comunidade. <br><br>

						Após a associação receber os computadores doados pelo projeto PIDS Tech, 
						foi realizado a montagem do 
						laboratório que atenderia as crianças, adolescentes e demais pessoas da comunidade<br><br>		

						Ao longo desses 10 anos de projeto, foram doados centenas de computadores para os 
						nossos alunos.	
					 </p>		
			
			<hr class="hr1">
			
			<p class="pText">
				<br>Em fevereiro de 2022, através da equipe de marketing da escola Senac Tech, foi 
				realizado uma entrevista na RBS TV, no Bom dia Rio Grande, com o objetivo de divulgar 
				o projeto PITS Tech, solicitando para toda comunidade, doação de equipamentos 
				eletrônicos para serem recuperados pelo projeto PIDS Tech.<br><br>
			</p>
					
			<p class="rep"> 
				<a href="https://globoplay.globo.com/v/10328310/">Caso queira ver a reportagem clique aqui para assistir	!</a>
			</p>
			
			<p class="pText">
				Além dos alunos e instituições que receberam doação de computadores, o projeto 
				PIDS Tech, também realizou a doação para os colaboradores da escola Senac Tech, que 
				não tinham condições de adquirir um equipamento eletrônico e para uma 
				colaboradora da escola Senac Saúde.
			</p>
			
			<p class="pText">
				Outros alunos que também foram prestigiados com a doação de computadores, 
				foram os alunos da primeira turma do projeto Ilê Tech. 
				Ao total, três alunos do projeto, receberam computadores para dar continuidade aos 
				seus estudos.
			</p>

								<p class = "pTitulo"> Objetivo Geral: </p>
						
							<p class="pText">
								Realizar a manutenção e doação de equipamentos eletrônicos.
							</p>
							
			<hr class="hr1">
			
					<p class = "pTitulo"> Objetivo específico: </p>
							
					<p class="pText">
							
								- Diminuir a exclusão digital dos alunos ou funcionários do Senac Tech; <br>
								- Realizar a recuperação ou implantação de laboratórios digitais em ONG’S, Associações ou escolas públicas ou municipais. <br>
								- Firmar parceria entre empresas privadas ou públicas para conseguir equipamentos eletrônicos; <br>
								- Incentivar alunos da escola Senac Tech, a participarem do projeto; <br>
								- Diminuir os índices de cancelamento de curso, através do projeto; <br>
								- Aumentar a receita da escola, para adquirir novos alunos; <br>
								- Incentivar outras escolas da rede Senac, a adotarem o projeto PIDS Tech. 						
						</p>
			<hr class="hr1">
			
			<p class="pTitulo">
			Projetos Futuros!
			</p>
			
			<p class="pText">
				A meta do projeto PIDS Tech para o futuro, é ampliar suas ações sustentáveis e 
				sociais, atingindo um público maior de pessoas beneficiadas pelo projeto. 
				Abaixo, algumas ações futuras para o projeto: 
				<ul class="pText">
				
				  <li>Criação de sistemas para associações ou ONG’S, utilizando o conhecimento técnico dos alunos da escola Senac Tech;</li>
				  <li>Manutenção update de sistemas de associações ou ONG’S;</li>
				  <li>Suporte a pequenas empresas, com ações relacionadas a parte administrativa ou marketing.</li>
				</ul>
				
			</p>
			
